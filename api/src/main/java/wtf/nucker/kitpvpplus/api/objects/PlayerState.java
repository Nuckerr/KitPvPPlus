/**
 * @author Nucker
 * Used for seeing the state of a player
 */
public enum PlayerState {

    /**
     * The player is at spawn
     */
    SPAWN,

    /**
     * The player is in the arena
     */
    ARENA,

    /**
     * @deprecated NOT FULLY IMPLEMENTED
     * The player is in a protected region
     */
    PROTECTED
}
